/*
1. String:
represents textual data enclosed in single or double quotes.
example: "aditya , shende"

2. Number:
represents numeric data ,including integers and floating-point numbers.
example: '50' or '6.69'

3. Boolean:
Represents a logical entity and can have two values: true or false.
Example: true

4. Undefined:
Represents a variable that has been declared but not assigned a value.
Example: let x; (Here, x is declared but holds an undefined value until it's assigned something.)

5. Null:
Represents the intentional absence of any object value.
Example: let y = null; (Here, y is explicitly assigned null to denote no value.)
*/